package org.Intutech.Been;

public class Notes {
	private int sr;
	private String tital;
	private String dis;
	
	public int getSr() {
		return sr;
	}
	public void setSr(int sr) {
		this.sr = sr;
	}
	public void setTital(String tital) {
		this.tital = tital;
	}
	public String getTital() {
		return tital;
	}
	public String getDis() {
		return dis;
	}
	public void setDis(String dis) {
		this.dis = dis;
	}
}
